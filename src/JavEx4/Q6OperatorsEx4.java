package JavEx4;

public class Q6OperatorsEx4 {

    public static void main (String [] args ) {

        double num1 = 7, num2=3.5;
        double addNum=num1 + num2;
        double moduNum=num1%num2;
        double multiNum=num1*num2;
        double divNum=num1/num2;


        //Arithmetic operators
        System.out.println("Addition: " +" "+ addNum);
        System.out.println("Multiplication: " +" "+ multiNum);
        System.out.println("Modulus: " +" "+ moduNum);
        System.out.println("Division: " +" "+ divNum);
    }
}

