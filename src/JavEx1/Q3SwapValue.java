package JavEx1;

public class Q3SwapValue {

    public static void main (String[]args ){


        int num10 = 10;
        int num20 = 20;
        int temp = 0;

        temp=10;
        System.out.println("Temp Value : " + temp);

        num10=num20;
        System.out.println("Num10 Value : " + num10);

        num20=temp;

        System.out.println("Num20 value : " + num20);



    }

}
